import fs from 'fs';
import path from 'path';
import { fileURLToPath, pathToFileURL } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = process.env.BENCHMARK_REPO_ROOT || path.resolve(__dirname, '..', '..');

export function updateBenchmarksReadme() {
  const benchRoot = path.join(REPO_ROOT, 'Benchmarks');
  fs.mkdirSync(benchRoot, { recursive: true });

  const platforms = [
    { dir: 'web-pwa', label: 'Web / PWA' },
    { dir: 'github-pages', label: 'GitHub Pages (equivalent build)' },
    { dir: 'capacitor-web', label: 'Capacitor (legacy web bundle)' },
    { dir: 'expo-rn', label: 'Expo / React Native (bundle stats)' },
    { dir: 'server', label: 'Python server (optional / local)' },
  ];

  let md = `# Benchmarks\n\n`;
  md += `Latest reports are generated by \`npm run benchmark\` (local) and by CI (artifacts + commits on \`main\`/\`master\`).\n\n`;
  md += `| Platform | Latest report |\n| --- | --- |\n`;

  for (const p of platforms) {
    const rel = `${p.dir}/latest.md`;
    const full = path.join(benchRoot, rel);
    const link = fs.existsSync(full) ? `[${p.label}](./${rel})` : `${p.label} _(run benchmarks to generate)_`;
    md += `| ${p.label} | ${link} |\n`;
  }

  md += `\n## Run locally\n\n`;
  md += `From the repository root:\n\n`;
  md += `1. \`npm ci\`\n`;
  md += `2. \`npm run build:web:apk\` (minified PWA) and \`npm run build:react\` (Capacitor \`dist/\`) as needed.\n`;
  md += `3. \`npx playwright install chromium\` once (from \`benchmark-runner/\` workspace).\n`;
  md += `4. \`npm run benchmark\` — writes Markdown under \`Benchmarks/\`.\n`;

  fs.writeFileSync(path.join(benchRoot, 'README.md'), md, 'utf8');
}

const isMain =
  process.argv[1] && import.meta.url === pathToFileURL(path.resolve(process.argv[1])).href;
if (isMain) {
  updateBenchmarksReadme();
}
